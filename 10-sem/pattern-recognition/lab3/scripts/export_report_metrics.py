def _cell_sources(nb: dict) -> list[str]:
    out = []
    for c in nb["cells"]:
        if c.get("cell_type") == "code":
            out.append("".join(c.get("source", [])))
    return out


def _find_cell(cells: list[str], predicate) -> str:
    for s in cells:
        if predicate(s):
            return s
    raise RuntimeError("required notebook code cell not found")


def _set_generation(src: str, generation: str) -> str:
    if generation not in ("separated", "overlap"):
        raise ValueError(generation)
    lines = []
    for line in src.splitlines():
        if re.match(r"^\s*GENERATION\s*=", line):
            lines.append(f'GENERATION = "{generation}"')
        else:
            lines.append(line)
    return "\n".join(lines) + "\n"


def notebook_namespace(generation: str) -> dict:
    """Повний namespace після імпортів, геометрії та клітини даних (як у ноутбуці)."""
    nb = json.loads(NB_PATH.read_text(encoding="utf-8"))
    cells = _cell_sources(nb)
    imp = _find_cell(cells, lambda s: "import numpy as np" in s and "matplotlib" in s)
    helpers = _find_cell(cells, lambda s: "class Petunin2DModel" in s and "def cross" in s)
    data = _find_cell(cells, lambda s: "CLASS_SPECS_SEPARATED" in s)
    data = _set_generation(data, generation)

    ns: dict = {}
    exec(imp, ns, ns)
    exec(helpers, ns, ns)
    buf = io.StringIO()
    old = sys.stdout
    try:
        sys.stdout = buf
        exec(data, ns, ns)
    finally:
        sys.stdout = old
    return ns


def _accuracy(results: list, pred_key: str) -> int:
    return sum(1 for r in results if r[pred_key] == r["true"])


def _run_pipeline(generation: str) -> dict:
    ns = notebook_namespace(generation)

    results = ns["classify_many"](
        ns["test_points"],
        ns["centroids"],
        ns["class_points"],
        ns["petunin_models_2d"],
        ns["test_points_3d"],
        ns["centroids_3d"],
        ns["petunin_models_3d"],
        ns["test_labels"],
    )
    names = list(ns["class_points"].keys())
    mismatch = [r for r in results if r["pred_L2"] != r["pred_L1"]]
    mismatch_pet = [r for r in results if r["pred_L2"] != r["pred_pet"]]
    mismatch_pet3 = [r for r in results if r["pred_L2_3d"] != r["pred_pet_3d"]]
    n = len(results)
    counts_l2 = {t: sum(1 for r in results if r["pred_L2"] == t) for t in names}
    counts_l1 = {t: sum(1 for r in results if r["pred_L1"] == t) for t in names}
    counts_pet = {t: sum(1 for r in results if r["pred_pet"] == t) for t in names}
    counts_l2_3d = {t: sum(1 for r in results if r["pred_L2_3d"] == t) for t in names}
    counts_pet3 = {t: sum(1 for r in results if r["pred_pet_3d"] == t) for t in names}
    centroids = ns["centroids"]
    return {
        "generation": generation,
        "n_test": n,
        "seed": int(ns.get("SEED", 24)),
        "n_per_class": int(ns.get("N_PER_CLASS", 100)),
        "acc_l2": _accuracy(results, "pred_L2"),
        "acc_l1": _accuracy(results, "pred_L1"),
        "acc_pet": _accuracy(results, "pred_pet"),
        "acc_l2_3d": _accuracy(results, "pred_L2_3d"),
        "acc_pet3": _accuracy(results, "pred_pet_3d"),
        "mismatch_l2_l1": len(mismatch),
        "mismatch_l2_pet": len(mismatch_pet),
        "mismatch_l2_pet3d": len(mismatch_pet3),
        "counts_l2": counts_l2,
        "counts_l1": counts_l1,
        "counts_pet": counts_pet,
        "counts_l2_3d": counts_l2_3d,
        "counts_pet3": counts_pet3,
        "centroids": {k: (float(v[0]), float(v[1])) for k, v in centroids.items()},
    }


