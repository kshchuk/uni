    def ellipsoid_parametric(center, half, rho, n_u=16, n_v=32):
        u = np.linspace(0.0, np.pi, n_u)
        v = np.linspace(0.0, 2.0 * np.pi, n_v)
        U, V = np.meshgrid(u, v)
        nx = np.sin(U) * np.cos(V)
        ny = np.sin(U) * np.sin(V)
        nz = np.cos(U)
        X = center[0] + rho * half[0] * nx
        Y = center[1] + rho * half[1] * ny
        Z = center[2] + rho * half[2] * nz
        return X, Y, Z

    def plot_polyline3d(ax, wx, wy, wz, color, lw=0.7):
        xs, ys, zs = [], [], []
        for x, y, z in zip(wx, wy, wz):
            if x is None:
                if xs:
                    ax.plot(xs, ys, zs, color=color, linewidth=lw, alpha=0.75)
                xs, ys, zs = [], [], []
            else:
                xs.append(x)
                ys.append(y)
                zs.append(z)
        if xs:
            ax.plot(xs, ys, zs, color=color, linewidth=lw, alpha=0.75)

    def bisector_plane_mesh(c0, c1, span, n=14):
        m = 0.5 * (c0 + c1)
        nvec = c1 - c0
        ln = np.linalg.norm(nvec)
        if ln < 1e-12:
            return None, None, None
        nvec = nvec / ln
        aux = np.array([0.0, 0.0, 1.0]) if abs(nvec[2]) < 0.9 else np.array([1.0, 0.0, 0.0])
        e1 = np.cross(nvec, aux)
        e1 = e1 / (np.linalg.norm(e1) + 1e-15)
        e2 = np.cross(nvec, e1)
        su = np.linspace(-span, span, n)
        sv = np.linspace(-span, span, n)
        U, V = np.meshgrid(su, sv)
        X = m[0] + U * e1[0] + V * e2[0]
        Y = m[1] + U * e1[1] + V * e2[1]
        Z = m[2] + U * e1[2] + V * e2[2]
        return X, Y, Z

